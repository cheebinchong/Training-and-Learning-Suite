const fs = require("fs");
const path = require("path");

module.exports = {
  attributes: {
    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    name: {
      type: "string",
      required: true
    },
    uid: {
      type: "string",
      unique: true
    },
    path: {
      type: "string",
      required: true,
      unique: true
    },
    width: {
      type: "number"
    },
    height: {
      type: "number"
    },

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝

    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝
    dataset: {
      model: "dataset"
    },

    labels: {
      collection: "LabelData",
      via: "file"
    }
  },
  async beforeDestroy(criteria, cb) {
    const records = await UploadedFile.find(criteria);

    try {
      records.map(async record => {
        // delete label data
        await LabelData.destroy({
          file: record.id
        });

        // delete the file
        const filePath = path.resolve(record.path);
        fs.unlinkSync(filePath);
      });
      return cb();
    } catch (error) {
      return cb(error);
    }
  }
};
